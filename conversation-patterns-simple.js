// 挨拶パターンチェック関数
function checkGreeting(userMessage) {
    const trimmed = userMessage.trim();
    
    // 完全一致を最優先でチェック
    if (exactMatchPatterns[trimmed]) {
        return {
            priority: 1000,
            response: exactMatchPatterns[trimmed]
        };
    }
    
    return null;
}

